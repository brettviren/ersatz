# some units

# time
second=1
millisecond=1e-3*second
microsecond=1e-6*second
nanosecond=1e-9*second
ms=millisecond
us=microsecond
ns=nanosecond

# size
bit=1
B=8*bit
Byte=B
kB=1e3*Byte
MB=1e6*Byte
Gbit=1e9*bit

# data rate
baud=bit/second
GB=1e9*Byte
Gbps=1e9*baud

# rate
Hz = 1.0/second
kHz = 1000*Hz
MHz = 1000*kHz
GHz = 1000*MHz
